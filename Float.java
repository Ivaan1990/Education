class FloatDemo {
	public static void  main(String[] args){
		int n;
		float f;
		float g;
		float h;
		float j;
		double k;
		n = 6;
		f = n;
		g = f/4;
		h = n/4;
		j = (float) n/4;
		k = n/4.0;
		System.out.println("n is " + n);
		System.out.println("f is " + f);
		System.out.println("g is " + g);
		System.out.println("h is " + h);
		System.out.println("j is " + j);
		System.out.println("k is " + k);
	}
}